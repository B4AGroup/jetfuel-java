package com.eixox.ui;

public class UIControlPresentation {

	private String label;
	private String placeholder;
	private String hint;
	private String message;
	private Object value;
	private UIControlOptionList options;
	private UIControlType controlType;
	private UIControlState state;
	private String id;
	private String name;
	
	/**
	 * @return the label
	 */
	public final String getLabel() {
		return label;
	}
	/**
	 * @param label the label to set
	 */
	public final void setLabel(String label) {
		this.label = label;
	}
	/**
	 * @return the placeholder
	 */
	public final String getPlaceholder() {
		return placeholder;
	}
	/**
	 * @param placeholder the placeholder to set
	 */
	public final void setPlaceholder(String placeholder) {
		this.placeholder = placeholder;
	}
	/**
	 * @return the hint
	 */
	public final String getHint() {
		return hint;
	}
	/**
	 * @param hint the hint to set
	 */
	public final void setHint(String hint) {
		this.hint = hint;
	}
	/**
	 * @return the message
	 */
	public final String getMessage() {
		return message;
	}
	/**
	 * @param message the message to set
	 */
	public final void setMessage(String message) {
		this.message = message;
	}
	/**
	 * @return the value
	 */
	public final Object getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public final void setValue(Object value) {
		this.value = value;
	}
	/**
	 * @return the options
	 */
	public final UIControlOptionList getOptions() {
		return options;
	}
	/**
	 * @param options the options to set
	 */
	public final void setOptions(UIControlOptionList options) {
		this.options = options;
	}
	/**
	 * @return the controlType
	 */
	public final UIControlType getControlType() {
		return controlType;
	}
	/**
	 * @param controlType the controlType to set
	 */
	public final void setControlType(UIControlType controlType) {
		this.controlType = controlType;
	}
	/**
	 * @return the state
	 */
	public final UIControlState getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public final void setState(UIControlState state) {
		this.state = state;
	}
	/**
	 * @return the id
	 */
	public final String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public final void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public final String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public final void setName(String name) {
		this.name = name;
	}
	
	
	
	
	
}
